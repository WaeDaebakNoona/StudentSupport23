package Backend;

/**
 *
 * @author Narita
 */
public class Message {
    
    private String topic;
    private String note;

    public Message(String topic, String note) {
        this.topic = topic;
        this.note = note;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
    
}
